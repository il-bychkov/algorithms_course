#include <stdio.h>
int main()
{
    unsigned int aBin = 0b1010;    
    unsigned int aOct = 0101;    
    unsigned int aHex = 0xFF;
    
    printf("Binary   0b1010 = %d\n", aBin);
    printf("Oct        0101 = %d\n", aOct);
    printf("Hex        0xFF = %d\n", aHex);
    
    return 0;    
}

